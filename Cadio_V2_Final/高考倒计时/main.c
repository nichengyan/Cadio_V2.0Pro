uint Day_Left(time tx){
    uint16_t DayCnt=0;
    while(1){
        if(tx.year==25&&tx.month==6&&tx.day==6) return DayCnt;
        if(tx.day>Days[tx.month]){ tx.day=1;tx.month++;}
        if(tx.month>12) {tx.year++;tx.month=1;}
        tx.day++;
        DayCnt++;
    }
}
